    public void listModel() {
        DefaultListModel dlm = new DefaultListModel();
       
        try {
            Scanner ipconfig = new Scanner(new File("./config.txt"));
            String host = "";
            while(ipconfig.hasNextLine()) {
                host = ipconfig.nextLine();
            }
            Registry registry = LocateRegistry.getRegistry(host, 1099);
            EvalInterface ei = (EvalInterface) registry.lookup("eval");
            
            ArrayList<String> inst = ei.fetchInstructors();
            Collections.sort(inst);
            for(String names:inst) {
                dlm.addElement(names);
            }
            
            
        }catch(Exception e) {
            System.out.println("Error");
            e.printStackTrace();
        }
        
        jList1.setModel(dlm);
        jList2.setModel(dlm);
    }

   

    

